CREATE FUNCTION [dbo].[udf_ConstructYearCode]
(
)
RETURNS int
AS
BEGIN

	declare @year varchar(4), @yearCode int

	select @year = datepart(yy, getdate())

	set @year = substring(@year, 3, 2)

	set @yearCode = cast(@year as int)

	return @yearCode

